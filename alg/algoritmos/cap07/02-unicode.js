let codigo_a = 97
// convertendo de código numérico para letra
let letra_a = String.fromCharCode(codigo_a)

console.log("\nLetra:", letra_a)
console.log("Código numérico:", codigo_a)

let letra_j = 'j'
// convertendo de letra para código numérico
let codigo_j = letra_j.charCodeAt()

console.log("\nLetra:", letra_j)
console.log("Código numérico:", codigo_j)